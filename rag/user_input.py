# ======================================================
# 終端機選項工具（強制選填）
# ======================================================

def select_option(title: str, options: Dict[str, str]) -> str:
    while True:
        print(f"\n{title}")
        for key, label in options.items():
            print(f"{key}. {label}")
        choice = input("請輸入選項編號：").strip()

        if choice in options:
            return options[choice]

        print("輸入錯誤，請重新選擇！")




